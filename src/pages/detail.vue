			this.datas.info.price=this.typePrice[index];
			this.datas.info.chooseType=this.datas.info.types[index];
			// 设置当前所选择的商品数据
			this.typeActive=index;
		}
	},
	components:{
		DetailSwiper,
		BaseLine,
		DetailFooter
	}
}

</script>
